public class Pessoa {
    private String nome, endereco;

    public Pessoa (String nome){
        this.nome=nome;
    }

    public String getNome(){
        return nome;
    }

    public void setNome(){
        this.nome=nome;
    }

     public String getEndereco(){
        return endereco;
    }

    public void setEndereco(){
        this.endereco=endereco;
    }
}
